package java_Classes;

public class calculater {
	public int Sum(int num1 , int num2) {
		return num1+num2;
	}
	public int Minus(int num1 , int num2) {
		return num1-num2;
	}
	public int multip(int num1 , int num2) {
		return num1*num2;
	}
	public int Divide(int num1 , int num2) {
		return num1/num2;
	}
}

